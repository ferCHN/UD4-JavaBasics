
public class Ex4_VariableEnteraNApp {

	public static void main(String[] args) {
		
		//Variable inicial
		int N = 1;
		
		//Extraer resultados de las operaciones con N
		int result1 = N+77;
		int result2 = result1-3;
		int result3 = result2*2;
		
		//Mostrar en consola los resultados de la variable N
		System.out.println("Valor inicial de N = "+N);
		System.out.println("N + 77 = "+result1);
		System.out.println("N - 3 = "+result2);
		System.out.println("N * 2 = "+result3);
	}

}
