
public class Ex1_DosVariablesApp {

	public static void main(String[] args) {
		
		int num1 = 8;
		int num2 = 4;
		
		//Declaro las varables que asigno a los calculos matematicos
		int suma = num1+num2;
		int resta = num1-num2;
		int multiplicacion = num1*num2;
		int division = num1/num2;
		int modulo = num1%num2;
		
		//Muestro por pantalla los calculos matematicos
		System.out.println("La suma de los dos numeros es: "+suma);
		System.out.println("La resta de los dos numeros es: "+resta);
		System.out.println("La multiplicacion de los dos numeros es: "+multiplicacion);
		System.out.println("La division de los dos numeros es: "+division);
		System.out.println("El modulo de los dos numeros es: "+modulo);
	}

}
